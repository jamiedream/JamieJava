package tw.org.iii.mesa0515;

public class LAB1506 {

	public static void main(String[] args) {
		byte a = 10, b = 3;
		int c = a+b;
		//byte, short, int 加減乘除取餘數只能用int呼叫
		System.out.println(c);
		
		double d = a/b;
		System.out.println(d);
		
		double e = 10, f = 3;
		double g = e/f;
		//型別double用double做運算
		System.out.println(g);
		
		
	}

}
